package ru.pb.auth.network.gameserver;

import io.netty.channel.ChannelInitializer;
import io.netty.channel.socket.SocketChannel;
import io.netty.handler.codec.serialization.ClassResolvers;
import io.netty.handler.codec.serialization.ObjectDecoder;
import io.netty.handler.codec.serialization.ObjectEncoder;
import io.netty.handler.logging.LogLevel;
import io.netty.handler.logging.LoggingHandler;

/**
 * Инициализатор каналов
 *
 * @author sjke
 */
public class GameChannelInitializer extends ChannelInitializer<SocketChannel> {

	@Override
	public void initChannel(SocketChannel channel) throws Exception {
		channel.pipeline().addLast(
				new ObjectEncoder(),
				new ObjectDecoder(ClassResolvers.cacheDisabled(ClassLoader.getSystemClassLoader())),
				new LoggingHandler(LogLevel.DEBUG),
				new GameConnection()
		);
	}

}
