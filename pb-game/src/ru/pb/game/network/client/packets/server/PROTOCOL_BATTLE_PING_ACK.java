package ru.pb.game.network.client.packets.server;

import ru.pb.game.network.client.packets.ServerPacket;

public class PROTOCOL_BATTLE_PING_ACK extends ServerPacket {

	private byte[] bytes;

	public PROTOCOL_BATTLE_PING_ACK(byte[] bytes) {
		super(0xD11);
		this.bytes = bytes;
	}

	@Override
	public void writeImpl() {
		writeB(bytes);
	}
}